	<h1>Republica Vira-Lata</h1>

	<p>A Vira-Lata fundada em 2 de setembro de 2005 por alunos do mais novo   campus da Universidade Federal de Ouro Preto, UFOP, o campus João   Monlevade. Na época os calouros de engenharia de produção Fabrício   (Salsicha), Juninho (Gepeto), Tiago (Baiano) e Giovanny decidiram ir   morar juntos para reduzir os custos, e assim formando a mais nova   república de João Monelvade - MG. Em busca de um nome para tal foi em   uma viagem dentro do ônibus de volta para casa que Juninho teve a ideia   do nome Vira-Lata. Por aprovação de todos os membros, assim firmou-se a   mais nova família monlevadense, a República Vira-Lata.<br>
	Primeiramente   situada em um apartamendo do prédio da Dona Célia, rua Angelina Ponce nº   97, e depois de uma temporada em Londres - ING, a república mudou-se   para onde é situada ainda hoje.<br>
	E   continua sua história em João Monlevade recolhendo estudantes da UFOP, e   fazendo dessa a segunda família de cada Vira-Lata que nela venha a   morar.</p>

